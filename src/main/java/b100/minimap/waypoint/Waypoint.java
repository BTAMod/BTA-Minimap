package b100.minimap.waypoint;

public class Waypoint {
	
	public String name;
	public int x;
	public int y;
	public int z;
	public int color;
	
	public Waypoint(String name, int x, int y, int z, int color) {
		this.name = name;
		this.x = x;
		this.y = y;
		this.z = z;
		this.color = color;
	}
	
}
